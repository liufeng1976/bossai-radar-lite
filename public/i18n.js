export function getLanguage() {
  const query = new URLSearchParams(window.location.search).get("lang");
  if (query === "zh" || query === "en") {
    localStorage.setItem(STORAGE_KEY, query);
    return query;
  }
  const saved = localStorage.getItem(STORAGE_KEY);
  if (saved === "zh" || saved === "en") return saved;
  return navigator.language?.toLowerCase().startsWith("zh") ? "zh" : "en";
}

export function setLanguage(language) {
  const normalized = language === "en" ? "en" : "zh";
  localStorage.setItem(STORAGE_KEY, normalized);
  const url = new URL(window.location.href);
  url.searchParams.set("lang", normalized);
  window.location.href = url.toString();
}

export function t(key, variables = {}, language = getLanguage()) {
  const template = dictionaries[language]?.[key] ?? dictionaries.zh[key] ?? key;
  return Object.entries(variables).reduce(
    (output, [name, value]) => output.replaceAll(`{${name}}`, String(value)),
    template,
  );
}

export function applyI18n(root = document, language = getLanguage()) {
  document.documentElement.lang = language === "en" ? "en" : "zh-CN";
  for (const element of root.querySelectorAll("[data-i18n]")) {
    element.textContent = t(element.dataset.i18n, {}, language);
  }
  for (const element of root.querySelectorAll("[data-i18n-placeholder]")) {
    element.setAttribute("placeholder", t(element.dataset.i18nPlaceholder, {}, language));
  }
  for (const element of root.querySelectorAll("[data-i18n-aria-label]")) {
    element.setAttribute("aria-label", t(element.dataset.i18nAriaLabel, {}, language));
  }
  for (const element of root.querySelectorAll("a[data-preserve-language]")) {
    const href = element.getAttribute("href");
    if (!href || href.startsWith("mailto:")) continue;
    const url = new URL(href, window.location.origin);
    url.searchParams.set("lang", language);
    element.setAttribute("href", `${url.pathname}${url.search}${url.hash}`);
  }
}

export function initLanguageToggle(selector = "#languageToggle") {
  const button = document.querySelector(selector);
  if (!button) return;
  const language = getLanguage();
  button.textContent = t("common.languageSwitch", {}, language);
  button.setAttribute("aria-label", t("common.languageLabel", {}, language));
  button.addEventListener("click", () => setLanguage(language === "zh" ? "en" : "zh"));
}

